import java.lang.*;     // to check size and the range of the datatypes


public class Sample 

{

	public static void main(String[] args)
	
	{
		System.out.println("The Integer minimum is : "+Integer.MIN_VALUE);
		System.out.println("The Integer maximum is : "+Integer.MAX_VALUE);
		System.out.println("The byte value is "+Byte.BYTES);
		System.out.println("The Byte minimum is : "+Byte.MIN_VALUE);
		System.out.println("The Byte maximum is : "+Byte.MAX_VALUE);
		System.out.println("The size of the Long data type is "+Long.SIZE);
		System.out.println("The size of the float data type is "+Float.SIZE);
		System.out.println(Short.MIN_VALUE);
		System.out.println(Short.MAX_VALUE);

	}

}


